# Basic Data Types in Python

# String
name = "Andi"

# Integer
age = 25

# Float
height = 1.75

# Boolean
is_graduated = True

# List
subjects = ["Math", "Science", "History"]

# Tuple
coordinates = (10.0, 20.0)

# Dictionary
student_info = {
  "name": "Andi",
  "age": 25,
  "is_graduated": True
}

# Set
unique_numbers = {1, 2, 3, 4, 5}

# NoneType
empty_value = None

# Print the types of the variables
print(f"Name: {name}, Type: {type(name)}")
print(f"Age: {age}, Type: {type(age)}")
print(f"Height: {height}, Type: {type(height)}")
print(f"Is Graduated: {is_graduated}, Type: {type(is_graduated)}")
print(f"Subjects: {subjects}, Type: {type(subjects)}")
print(f"Coordinates: {coordinates}, Type: {type(coordinates)}")
print(f"Student Info: {student_info}, Type: {type(student_info)}")
print(f"Unique Numbers: {unique_numbers}, Type: {type(unique_numbers)}")
print(f"Empty Value: {empty_value}, Type: {type(empty_value)}")
