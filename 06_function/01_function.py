# Function in Python
def greet(name):
    """Function to greet a person with their name."""
    return f"Hello, {name}!"

# Example usage of the function
print(greet("Andi"))  # Output: Hello, Andi!


# Function to add two numbers
def add_numbers(a, b):
    """Function to add two numbers."""
    return a + b

# Example usage of the add_numbers function
result = add_numbers(5, 10)
print(f"The sum of 5 and 10 is: {result}")  # Output: The sum of 5 and 10 is: 15


# Function to calculate the area of a rectangle
def calculate_area(length, width):
    """Function to calculate the area of a rectangle."""
    return length * width

# Example usage of the calculate_area function
area = calculate_area(5, 3)
print(f"The area of the rectangle is: {area}")  # Output: The area of the rectangle is: 15
